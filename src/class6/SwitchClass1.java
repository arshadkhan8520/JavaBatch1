package class6;

public class SwitchClass1 {

	public static void main(String[] args) {
		
		String country = "Afghanistan";
		
		switch (country) {
		
		case "USA":
		System.out.println("You are an American");
		break;
		
		case "Russia":
		System.out.println("You are Russian");
		break;
		
		case "Afghanistan":
		System.out.println("You are proud Afghan");
		break;
		default:
		System.out.println("I don't know your Nationality");
		}




	}

}
