package class3;

public class AdditionVsConcatenation {
	
	public static void main(String []args) {
		
		int number1 = 5;
		int number2 = 10;
		
		System.out.println(number1 + number2);
		
		String str1 = "Hello";
		String str2 =  "Hi";
		
		System.out.println(number1 + number2 + str1 + str2);
	}

}
