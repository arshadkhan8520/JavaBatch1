package class3;

public class ShortHandOperators {

	public static void main(String[]args) {
		
		int num1 = 10;
		int num2 = 40;
		num1=num1+10;
		num1+=10;
		System.out.println(num1);
		
		num1-=10;
		System.out.println(num1);
		
		num2/=10;
		System.out.println(num2);
		
		// Declare variable and increase by 100 using short-hand operator
		
		int number=0;
		number +=100;
		System.out.println(number);
		
		// Declare variable and decrease by -67 using short-hand operator
		int number1=67;
		number -=67;
		System.out.println(number1);
		
		
	}

}
