package class12;

public class StringUpperLowerCase {

	public static void main(String[] args) {
		
		// changing to lower case
		
		String hello = "HELLO";
		
		String lowerCaseHello =hello.toLowerCase();
		System.out.println(lowerCaseHello);
		
		// Now change back to upper case
		
		System.out.println(lowerCaseHello.toUpperCase());



	}

}
