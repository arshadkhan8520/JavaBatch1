package class12;

public class EqualString {

	public static void main(String[] args) {
		
		// Equal string
		
		String s1 = "Hello";
		String s2 = "Do not worry you will get a job";
		String s3 = "hello";
		
		System.out.println(s1.equals(s2)); // the out put will be boolean
		
		System.out.println(s1.equalsIgnoreCase(s3)); // this ignore the upper and lower case
		

	}

}
