package class11;

public class ArrayElementAdvanceForLoop {

	public static void main(String[] args) {
		
		
		// How to sum all numbers in Array Element Advance For Loop.
		
		int [] num = {12, 10, 15, 16, 18};
		int sum=0;
		
		for (int n:num) {
			sum=sum+n;
				
		}
		
		System.out.println("Array elements sum :"+sum);

	}

}
