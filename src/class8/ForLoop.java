package class8;

public class ForLoop {
	
	public static void main (String[]args) {
		
		// I want to say 3 times hello to Afghnistan.
		
	//	System.out.println("Hello Afghanistan");
	//	System.out.println("Hello Afghanistan");
	//	System.out.println("Hello Afghanistan");
		
		for (int i=1; i<=3; i++) {
			System.out.println("Hello Afghanistan");
			
		}
		
		System.out.println(">>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>");
		
		// Print the number from 1 -50 in one line
		
		for (int k=1; k<=50; k++) {
			System.out.print(k+"  ");
		
		}
		System.out.println(">>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>");
		
		// print number from 50-1
		
		for (int j=10; j>=1; j--) {
			System.out.print(j+" ");
		
		}
		System.out.println(">>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>");
		
		// print number from 1-20 increment by 2
		
		for (int m=1; m<20; m+=2) {
			System.out.print(m+" ");
		
	}
	}
}
