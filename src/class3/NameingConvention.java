package class3;

public class NameingConvention {
	public static void main(String[]args) {
		
		
		/*
		 * // we never use java keyword naming public static final. //Exmp: 
		 * int public =2;
		 * 
		 * When we name the variables, we should not have space between two words.
		 * Exp: String myLast name= "Ali";
		 * 
		 * We use camel casing for name the variables 
		 * Exp: 
		 */
		
		String numberButItIsString= "10";
		int num=5;
		System.out.println(numberButItIsString+num);
		
		//how to multiply
		int num2=2;
		int num3=4;
		System.out.println(num2*num3);
		
		// How to Addition
		System.out.println(num2+num3);
		
	}

}
